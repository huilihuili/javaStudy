public class PopcornPopper {

	public void on() {
		System.out.println("Popcorn popper on");
	}

	public void pop() {
		System.out.println("Popcorn Popper popping popcorn");
	}
	
	public void off() {
		System.out.println("popcorn popper off");
	}
}
